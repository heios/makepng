/**
 * Pick the best available locale for the user's preference list:
 * exact tag match first, then primary-subtag match (pt-BR → pt), first hit
 * wins; English if nothing matches.
 */
export function detectLocale(preferred: readonly string[], available: readonly string[]): string {
  for (const tag of preferred) {
    const lower = tag.toLowerCase();
    const exact = available.find((c) => c.toLowerCase() === lower);
    if (exact) return exact;
    const primary = lower.split("-")[0] as string;
    const byPrimary = available.find((c) => c.toLowerCase() === primary);
    if (byPrimary) return byPrimary;
  }
  return "en";
}

